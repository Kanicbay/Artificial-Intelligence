    private void btnAgregarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarVentaActionPerformed
        // TODO add your handling code here:
        int idVenta, idCliente, numerofactura;
        double valorNeto;
        boolean isIva; //1.12
        String fecha, descripcionProducto;

        idVenta = Integer.parseInt(txtIdVenta.getText());
        idCliente = Integer.parseInt(txtIdClienteV.getText());
        numerofactura = Integer.parseInt(txtNumeroFactura.getText());
        valorNeto = Double.parseDouble(txtValorNeto.getText());
        if(cmbIva.getSelectedItem().equals("True")){
            isIva=true;
        }else{
            isIva=false;
        }
        fecha = txtFechaV.getText();
        descripcionProducto = txtDescripcionProducto.getText();

        venta = new Ventas(idVenta, idCliente, numerofactura, valorNeto, isIva, fecha, descripcionProducto);
        ventas.add(venta);
    }//GEN-LAST:event_btnAgregarVentaActionPerformed

