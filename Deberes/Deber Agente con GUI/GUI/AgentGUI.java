    private void btnCrearClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearClienteActionPerformed
        // TODO add your handling code here:
        String nombre, apellido, direccion, telefono, correo, ciudad, pais;
        int id, idVendedor, zipcode, plazo;
        double monto;
        
        nombre = txtNombre.getText();
        apellido = txtApellido.getText();
        direccion = txtDireccion.getText();
        telefono = txtTelefono.getText();
        correo = txtCorreo.getText();
        ciudad = txtCiudad.getText();
        pais = txtPais.getText();
        id = Integer.parseInt(txtId.getText());
        idVendedor = Integer.parseInt(txtIdVendedor.getText());
        zipcode = Integer.parseInt(txtZip.getText());
        plazo = Integer.parseInt(txtPlazo.getText());
        monto = Double.parseDouble(txtMonto.getText());
     
        cliente = new Cliente(nombre, apellido, direccion, telefono, correo, ciudad, pais, id, idVendedor, zipcode, plazo, monto);
    }//GEN-LAST:event_btnCrearClienteActionPerformed

